//////////////////////////////////////////////////////////////////////////////
Hora::Hora()
{hora=0;
minuto=0;
segundo=0;
}

///////////////////////////////////////////////////////////////////////////////
Hora::Hora(int hor, int min, int seg)
{
hora=hor;
minuto=min;
segundo=seg;

}

/////////////////////////////////////////////////////////////////
Hora::~Hora()
{}
//////////////////////////////////////////////////////////////

Hora& Hora:: operator= (const Hora &obj)
{
hora=obj.hora;
minuto=obj.minuto;
segundo=obj.segundo;
return *this;
}

////////////////////////////////////////////////////////////////
Hora & Hora:: operator++ ()
{hora++;
minuto++;
segundo++;
return *this;
}

///////////////////////////////////////////////////////////////////

Hora Hora:: operator++(int a)
{Hora aux(*this);
hora++;
minuto++;
segudo++;
return aux;
}

